#de esta forma podemos extraer de un texto indicando el rango, recordando que inicia en cero y que los espacios cuentan
frase = 'controlar la complejidad es la escencia de la programacion'
print(frase[0:24])

#de esta forma  podemos extraer cada dos palabras o si queremos cada 3 o cada 4 etc
dicho = 'si no aprendo a programar no conseguire dinero'
print(dicho[0::2])

#de esta forma puedo extraer de forma invertida con el -1
realidad = 'debo acabar el dia 3 hoy porque quiero programar mucho'
print(realidad[::-1])