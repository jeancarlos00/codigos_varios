#con el metodo upper se ponen en mayusculas
frase = 'la economia esta buena en estos dias, balbaro y porque tu hija no estudia donde estudia la mia'
print(frase.upper())

# con el metodo lower se ponen en minusculas
dicho = 'HIJA NO ESTUDIA DONDE ESTUDIA LA MIA'
print(dicho.lower())

#con el metodo join podemos unir diferentes string
a = 'yo'
b = 'no'
c = 'tengo'
d = 'cualto'

print(" ".join([a,b,c,d]))

#el metodo replace sirve para reemplazar una palabra o un texto
mi_olla = 'cuando le page lo que debo ya no debere'

print(mi_olla.replace('le page','tenga').replace('lo que debo','dinero'))

