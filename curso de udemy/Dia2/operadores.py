x = 10
y = 2
z =9

print(f'{x} mas {y} es igual a {x+y}')
print(f'{x} menos {y} es igual a {x-y}')
print(f'{x} por {y} es igual a {x*y}')
print(f'{x} entre {y} es igual a {x/y}')

#dividido al piso osea sin el punto decimal
print(f'{z} dividido al piso de {y} es igual a {z//y}')

#modulo se usa tambien para saber cuando un numero es par porque cuando no sobra nada en una division el numero es par
print(f'{z} modulo de {y} es igual a {z%y}')

#elevar a la potencia al cuadrado
print(f'{x} elevado a la  {y} es igual a {x**y}')

#elevar al cubo
print(f'{x} elevado a {3} es igual a {x**3}')

#raiz cuadrada(se saca **0.5)
print(f'la raiz cuadrada de {49} es igual a {49**0.5}')